function flattenNavItems(items = []) {
  return items.flatMap((item) =>
    item.items?.length
      ? flattenNavItems(item.items)
      : item.href
        ? [{ label: item.label, href: item.href, badge: item.badge }]
        : []
  );
}

function firstNavHref(node) {
  if (node?.href) return node.href;
  for (const item of node?.items || []) {
    const href = firstNavHref(item);
    if (href) return href;
  }
  return undefined;
}

/** @deprecated use SIDEBAR_NAV — kept for any leftover imports */
export const NAV = SIDEBAR_NAV.map((n) => ({
  label: n.label,
  href: n.href || firstNavHref(n),
  badge: n.badge,
  items: n.items ? flattenNavItems(n.items) : n.href ? [{ label: n.label, href: n.href }] : [],
}));
